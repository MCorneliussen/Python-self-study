# input()

# Python stops executing when it comes down to the input() function and continues when the user has submitted some input.

name = input('Name: ') # the user types in a name and the value is stored in the variable.
age = input('Age: ') # you can have multiple inputs.

# after the user has input data the print function is called and will print out whatever was input.
print('Hello', name, 'you are', age, 'years old.') 
